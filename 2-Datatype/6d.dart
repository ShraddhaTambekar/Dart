void main(){              //Var datatype

  var x = 30;             //var datatype can store any datatype
  print(x);               //o-30

  var y = 30.87;
  print(y);               //o-30.87

  var z = "Shraddha";
  print(z);               //Shraddha

  var w = true;
  print(w);               //true

}